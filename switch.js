const color = 'yellow';
if (color === 'green') {
    console.log('this is green color');
}
else if (color === 'blue') {
    console.log('this is blue color');
}
else if (color === 'white') {
    console.log('this is white color');
}
else if (color === 'yellow') {
    console.log('this is yellow color');
}
else {
    console.log('this is black color');
}


// switch
switch (color) {
    case 'green':
        console.log('this is green color');
        break;
    case 'blue':
        console.log('this is blue color');
        break;
    case 'white':
        console.log('this is white color');
        break;
    case 'yellow':
        console.log('this is yellow color');
        break;
    default:
        console.log('this is black color');
}